package app.memoling.android.preference.custom;

public class WordOfTheDayTime {

	private int m_hours;
	private int m_minutes;

	public int getHours() {
		return m_hours;
	}

	public void setHours(int hours) {
		m_hours = hours;
	}

	public int getMinutes() {
		return m_minutes;
	}

	public void setMinutes(int minutes) {
		m_minutes = minutes;
	}

}
